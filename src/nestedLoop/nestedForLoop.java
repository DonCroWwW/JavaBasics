package nestedLoop;

/**
 * @author Legion
 *
 */
public class nestedForLoop {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		
		
		int i=0;
		int j;
		for(i=0;i<10;i++)
		{
			System.out.println(" ");
			for(j=10-i;j>0;j--)
			{
				System.out.print("J");
			}
		}

	}

}
